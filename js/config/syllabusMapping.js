// Utility functions for syllabus mapping
const SyllabusMappingUtils = {
    /**
     * Get all subjects in the syllabus
     * @returns {Array} Array of subject names
     */
    getAllSubjects() {
        return Object.keys(SYLLABUS_MAPPING).filter(key => key !== 'global_keywords');
    },

    /**
     * Get all chapters for a specific subject
     * @param {string} subject - Subject name
     * @returns {Array} Array of chapter names
     */
    getChaptersForSubject(subject) {
        return SYLLABUS_MAPPING[subject] ? SYLLABUS_MAPPING[subject].chapters : [];
    },

    /**
     * Get keywords for a specific chapter
     * @param {string} subject - Subject name
     * @param {string} chapter - Chapter name
     * @returns {Array} Array of keywords
     */
    getKeywordsForChapter(subject, chapter) {
        if (SYLLABUS_MAPPING[subject] && SYLLABUS_MAPPING[subject].keywords[chapter]) {
            return SYLLABUS_MAPPING[subject].keywords[chapter];
        }
        return [];
    },

    /**
     * Detect subject and chapter from question text using keyword matching
     * @param {string} questionText - The question text to analyze
     * @returns {Object} Object with detected subject and chapter
     */
    detectSubjectAndChapter(questionText) {
        const text = questionText.toLowerCase();
        let bestMatch = { subject: null, chapter: null, score: 0 };

        // Check each subject and chapter
        for (const subject of this.getAllSubjects()) {
            const chapters = this.getChaptersForSubject(subject);
            
            for (const chapter of chapters) {
                const keywords = this.getKeywordsForChapter(subject, chapter);
                let score = 0;

                // Count keyword matches
                for (const keyword of keywords) {
                    if (text.includes(keyword.toLowerCase())) {
                        score++;
                    }
                }

                // Update best match if this is better
                if (score > bestMatch.score) {
                    bestMatch = { subject, chapter, score };
                }
            }
        }

        return bestMatch;
    },

    /**
     * Get subject distribution for test generation
     * @returns {Object} Object with subject weights
     */
    getSubjectDistribution() {
        const subjects = this.getAllSubjects();
        const distribution = {};
        
        // Default equal distribution
        const weight = 1 / subjects.length;
        subjects.forEach(subject => {
            distribution[subject] = weight;
        });

        return distribution;
    },

    /**
     * Validate if a question belongs to specified subject/chapter
     * @param {string} questionText - Question text
     * @param {string} expectedSubject - Expected subject
     * @param {string} expectedChapter - Expected chapter (optional)
     * @returns {boolean} True if question likely belongs to the subject/chapter
     */
    validateQuestionMapping(questionText, expectedSubject, expectedChapter = null) {
        const detected = this.detectSubjectAndChapter(questionText);
        
        if (detected.subject !== expectedSubject) {
            return false;
        }

        if (expectedChapter && detected.chapter !== expectedChapter) {
            return false;
        }

        return detected.score > 0; // At least one keyword match
    }
};

